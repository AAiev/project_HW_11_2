def transform_in_upper(word):
    """
    Меняет буквы входящего слова на заглавные
    :param word: str
    :return: word.upper
    """
    return word.upper()

def transform_in_title(word):
    """
    Меняет первую букву входящего слова на заглавную
    :param word: str
    :return: word.title
    """
    return word.title()